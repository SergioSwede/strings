example = 'Some text'                   # Данная строка
print(example[0])                       # вывод первого символа в строке
print(example[-1])                      # вывод последнего символа в строке
print(example[int(len(example) / 2):])  # вывод с середины с нечетным количеством
print(example[::-1])                    # вывод строки наоборот
print(example[1::2])                    # вывод каждого второго символа
